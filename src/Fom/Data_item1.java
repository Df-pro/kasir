    private void showdata() {
    DefaultTableModel model = (DefaultTableModel)Tbl_Item.getModel();
    model.setRowCount(0);
    try {
        String sql = "SELECT * FROM tbl_dataitem";
        ps=conn.prepareStatement(sql);
        rs=ps.executeQuery(sql);
        while(rs.next()){  
            int no=rs.getInt("No");
            String nama=rs.getString("nama_item");
            String Kategori=rs.getString("Kategori");
            // Format harga dengan DecimalFormat dan titik sebagai pemisah
            double hargaDb = Double.parseDouble(rs.getString("harga"));
            DecimalFormat df = new DecimalFormat("#,###");
            df.setDecimalFormatSymbols(new DecimalFormatSymbols(new Locale("id")));
            String harga = df.format(hargaDb);
            
            Object[] Rowdata={no,nama,Kategori,harga};
            model.addRow(Rowdata);
        }
    } catch (Exception e) {
        System.out.println("Error: " + e.getMessage());
    }
    }

